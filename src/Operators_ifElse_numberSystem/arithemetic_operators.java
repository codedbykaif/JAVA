public class arithemetic_operators {
    public static void main(String [] args) {
//     integers arithemetic operations
        int a = 543;
        int b = 64;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        System.out.println("\n\n");

//        double arithemetic operations
        double c = 534;
        double d = 2;
        System.out.println(c+d);
        System.out.println(c-d);
        System.out.println(c*d);
        System.out.println(c/d);


;

    }
}