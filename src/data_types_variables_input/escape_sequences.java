public class escape_sequences {
    public static void main(String[] args){

        System.out.println("Hello 'kaif' dreamer");
        System.out.println("Hello \"kaif\" dreamer");
        System.out.println("Hello \\kaif\\ dreamer");
        System.out.println("Hello \nkaif dreamer");
        System.out.println("Hello \tkaif dreamer");
        System.out.println("Hello \bkaif dreamer");
    }
    }

